<H1 CLASS="topic">ltools - Manual</H1>
<P><I>ltools</I> is a suite of development tools which works equally well on Linux as well as on Windows.
<P>All the tools in the suite puts emphasis to simplicity and clarity before features and functionality. However, quite advanced things can be done with the tools and most of the time in a more easier way than how the tools the suite replaces does it.
<P>The tools are meant for developers. The tools are under constant development but backward compatibility will be retained whenever possible.

