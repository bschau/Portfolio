<H1 CLASS="topic">ltools</H1>
<P><I>ltools</I> is a suite of development tools which works equally well on Linux as well as Windows. The suite currently contains:
<DL>
<DT>lmake</DT>
<DD>A <TT>make</TT> replacement with focus on a simpler syntax and cross-platform functionality.</DD>
<DT>lcdoc</DT>
<DD>A <TT>javadoc</TT> for <B>c</B>.</DD>
<DT>lhd</DT>
<DD>A small hexdump utility.</DD>
<DT>lme</DT>
<DD>A macro expander.</DD>
</DL>

